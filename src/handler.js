const {nanoid} = require('nanoid');
const id = nanoid(16);
const books = require('./book');

const addBook = (request, h) => {
  const {name,year,author,summary,publisher,pageCount,readPage,reading,} = request.payload;
  const finished = pageCount === readPage;
  const insertedAt = new Date().toISOString();
  const updatedAt = insertedAt;


  // User tidak input name
  if (!name) {
    const response = h
      .response({
        status: 'fail',
        message: 'Gagal menambahkan buku. Mohon isi nama buku',
      })
      .code(400);
    return response;
  }

  // ReadPage yang lebih besar dari nilai properti pageCount
  if (readPage > pageCount) {
    const response = h
      .response({
        status: 'fail',
        message:'Gagal menambahkan buku. readPage tidak boleh lebih besar dari pageCount'
      })
      .code(400);
    return response;
  }

  const newBook = {
    name,
    year,
    author,
    summary,
    publisher,
    pageCount,
    readPage,
    reading,
    id,
    finished,
    insertedAt,
    updatedAt,
  };

  // push to books array
  books.push(newBook); 

  const isSuccess = books.filter((note) => note.id === id).length > 0;

  
  // Jika buku berhasil ditambahkan
  if (isSuccess) {
    const response = h
      .response({
        status: 'success',
        message: 'Buku berhasil ditambahkan',
        data: {
          bookId: id,
        },
      })
      .code(201);
    return response;
  }

  // Buku gagal ditambahkan
  const response = h
    .response({
      status: 'fail',
      message: 'Buku gagal ditambahkan',
    })
    .code(500);
  return response;
};

const getAll = (request, h) => {
  const { name, reading, finished } = request.query;

  if (!name && !reading && !finished) {
    const response = h
      .response({
        status: 'success',
        data: {
          books: books.map((book) => ({
            id: book.id,
            name: book.name,
            publisher: book.publisher,
          })),
        },
      })
      .code(200);

    return response;
  }

  if (name) {
    const BooksName = books.filter((book) => {
      const nameRegex = new RegExp(name, 'gi');
      return nameRegex.test(book.name);
    });

    const response = h
      .response({
        status: 'success',
        data: {
          books: BooksName.map((book) => ({
            id: book.id,
            name: book.name,
            publisher: book.publisher,
          })),
        },
      })
      .code(200);

    return response;
  }

  if (reading) {
    const BooksReading = books.filter(
      (book) => Number(book.reading) === Number(reading),
    );

    const response = h
      .response({
        status: 'success',
        data: {
          books: BooksReading.map((book) => ({
            id: book.id,
            name: book.name,
            publisher: book.publisher,
          })),
        },
      })
      .code(200);

    return response;
  }

  // kalau ada query finished
  const BooksFinished = books.filter(
    (book) => Number(book.finished) === Number(finished),
  );

  const response = h
    .response({
      status: 'success',
      data: {
        books: BooksFinished.map((book) => ({
          id: book.id,
          name: book.name,
          publisher: book.publisher,
        })),
      },
    })
    .code(200);

  return response;
};

const getBook = (request, h) => {
  const { bookId } = request.params;

  // find book by id
  const book = books.filter((n) => n.id === bookId)[0]; 

  // Bila id buku  ditemukan
  if (book) {
    const response = h
      .response({
        status: 'success',
        data: {
          book,
        },
      })
      .code(200);
    return response;
  }

  // Bila id buku tidak ditemukan
  const response = h
    .response({
      status: 'fail',
      message: 'Buku tidak ditemukan',
    })
    .code(404);
  return response;
};

const updateBook = (request, h) => {
  const { bookId } = request.params;

  const {name,year,author,summary,publisher,pageCount,readPage,reading} = request.payload;


  // User tidak mengisi name pada request body
  if (!name) {
    const response = h
      .response({
        status: 'fail',
        message: 'Gagal memperbarui buku. Mohon isi nama buku',
      })
      .code(400);
    return response;
  }

  
  // ReadPage yang lebih besar dari nilai properti pageCount
  if (readPage > pageCount) {
    const response = h
      .response({
        status: 'fail',
        message:
          'Gagal memperbarui buku. readPage tidak boleh lebih besar dari pageCount',
      })
      .code(400);
    return response;
  }

  const finished = pageCount === readPage;
  const updatedAt = new Date().toISOString();

  // find book by id
  const index = books.findIndex((note) => note.id === bookId); 

  if (index !== -1) {
    books[index] = {
      ...books[index],
      name,
      year,
      author,
      summary,
      publisher,
      pageCount,
      readPage,
      reading,
      finished,
      updatedAt,
    };

    // Bila buku berhasil diperbarui
    const response = h
      .response({
        status: 'success',
        message: 'Buku berhasil diperbarui',
      })
      .code(200);
    return response;
  }

  // idtidak ditemukkan oleh server
  const response = h
    .response({
      status: 'fail',
      message: 'Gagal memperbarui buku. Id tidak ditemukan',
    })
    .code(404);
  return response;
};

const deleteBook = (request, h) => {
  const { bookId } = request.params;

   // find book by id
  // const book = books.filter((n) => n.id === bookId)[0]; 
  const book = books.findIndex((note) => note.id === bookId); 

  if (book !== -1) {
    books.splice(book, 1);

    // Bila id buku berhasil dihapus
    const response = h
      .response({
        status: 'success',
        message: 'Buku berhasil dihapus',
      })
      .code(200);
    return response;
  }

  // Bila id yang dilampirkan tidak dimiliki oleh buku manapun
  const response = h
    .response({
      status: 'fail',
      message: 'Buku gagal dihapus. Id tidak ditemukan',
    })
    .code(404);
  return response;
};

